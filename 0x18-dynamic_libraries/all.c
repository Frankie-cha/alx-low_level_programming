#include <stdio.h>
/**
 * add - add two integers 
 * a: first int
 * b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction of two integers 
 * a: first int
 * b: second int
 * Return: subtracted
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - division of two integers 
 * a: first int
 * b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod of two intgers 
 * a: first int
 * b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiply two integers 
 * a: first int
 * b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
